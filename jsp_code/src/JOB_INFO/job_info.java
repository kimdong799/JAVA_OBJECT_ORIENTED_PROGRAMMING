package JOB_INFO;

public class job_info {

	private int job_info_id;
	private String job_info_title;
	private String user_id;
	private String job_info_date;
	private String job_info_content;
	public int getJob_info_id() {
		return job_info_id;
	}
	public void setJob_info_id(int job_info_id) {
		this.job_info_id = job_info_id;
	}
	public String getJob_info_title() {
		return job_info_title;
	}
	public void setJob_info_title(String job_info_title) {
		this.job_info_title = job_info_title;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getJob_info_date() {
		return job_info_date;
	}
	public void setJob_info_date(String job_info_date) {
		this.job_info_date = job_info_date;
	}
	public String getJob_info_content() {
		return job_info_content;
	}
	public void setJob_info_content(String job_info_content) {
		this.job_info_content = job_info_content;
	}
	
	
}
