package org.opentutotials.javatutorials.variables;

public class StringDemo {

	public static void main(String[] args) {
		String first;
		first = "coding";
		
		System.out.println(first);
		
		//변수선언과 값을 할당하는 것을 한번에 가능
		String second = "JAVA";
		System.out.println(second);
		
		//변수 두개를 한번에 데이터형식 선언 가능
		String a, b;
		a = "coding";
		b = "everybodyd";
		System.out.println(a+b);
	}

}
